export interface ProfileConfig {
  /**
   * Whether user can see bussiness page
   */
  allowSeeBussinesses?: boolean;
  /**
   * Whether user can see objects page
   */
  allowSeeObjects?: boolean;
  /**
   * Whether user can see bussiness selector
   */
  allowSeeBussinessSelector?: boolean;
  /**
   * Whether user can see objects selector
   */
  allowSeeObjectSelector?: boolean;
  /**
   * Whether user can see employees page
   */
  allowSeeEmployees?: boolean;
  /**
   * Whether user can add/edit employee
   */
  allowAddEmployee?: boolean;
}
